CREATE TABLE CMD_LOG(
  CMD_LOG_ID NUMBER GENERATED BY DEFAULT, 
  GUILD_ID NUMBER, 
  CMD_ID NUMBER, 
  CHANNEL_ID NUMBER, 
  USER_ID NUMBER, 
  HAS_PERMS NUMBER, 
  CMD_ERR_ID NUMBER, 
  CREATE_STAMP DATE DEFAULT sysdate, 
    CONSTRAINT CMD_LOG_PK PRIMARY KEY (CMD_LOG_ID), 
    CONSTRAINT CMD_LOG_SETTING_FK FOREIGN KEY (GUILD_ID) REFERENCES SETTING(GUILD_ID), 
    CONSTRAINT CMD_LOG_CMD_FK FOREIGN KEY (GUILD_ID, CMD_ID) REFERENCES COMMAND(GUILD_ID, CMD_ID), 
    CONSTRAINT CMD_LOG_CMD_ERR_FK FOREIGN KEY (CMD_ERR_ID) REFERENCES CMD_ERR(CMD_ERR_ID)
);

